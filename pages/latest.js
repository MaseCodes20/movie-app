import { useState, useEffect } from "react";
import { useRecoilState } from "recoil";
import { searchState } from "../atoms/searchAtom";
import Footer from "../components/Footer";
import Header from "../components/Header";
import Movies from "../components/Movies";
import PageNavButtons from "../components/PageNavButtons";
import SearchMovies from "../components/searchMovies/SearchMovies";
import Loading from "../components/Loading";
import Head from "next/head";
import { loadingState } from "../atoms/loadingAtom";
import { pageState } from "../atoms/pageAtom";

function Latest({ imageUrl }) {
  const [searchTerm, setSearchTerm] = useRecoilState(searchState);
  const [latestMovies, setLatestMovies] = useState([]);
  const [page, setPage] = useRecoilState(pageState);
  const [loading, setLoading] = useRecoilState(loadingState);

  const getLatestMovies = async () => {
    try {
      const response = await fetch(`/api/latest?page=${page}`, {
        headers: {
          Accept: "application/json",
          "Access-Control-Allow-Origin": "*",
        },
      })
        .then((response) => response.json())
        .then((response) => {
          setLatestMovies(response.data);
          setLoading(false);
        });

      return response;
    } catch (error) {
      console.error(error);
    }
  };

  useEffect(() => {
    window.scrollTo(0, 0);
    getLatestMovies();
    setSearchTerm("");
  }, [page]);

  return (
    <div className="pageContainer">
      <Head>
        <title>Latest</title>
        <meta name="description" content="Generated by Masecodes" />
        <link rel="icon" href="/cinema_popcorn_icon-icons.com_66128.ico" />
      </Head>
      <div className="contentContainer">
        <Header setSearchTerm={setSearchTerm} />
        {searchTerm ? (
          <SearchMovies image={imageUrl} />
        ) : (
          <>
            {loading ? (
              <>
                <Loading loadingMovies={loading} />
              </>
            ) : (
              <>
                {latestMovies.length > 0 && (
                  <>
                    <Movies movies={latestMovies} image={imageUrl} />
                    <PageNavButtons setPage={setPage} page={page} />
                  </>
                )}
              </>
            )}
          </>
        )}

        <Footer />
      </div>
    </div>
  );
}

export default Latest;

export async function getServerSideProps(context) {
  const imageUrl = process.env.IMG_URL;

  return {
    props: { imageUrl },
  };
}
